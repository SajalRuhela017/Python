#ARITHMATIC OPERATORS

print(5 + 2)            #addition
print(5 - 2)            #subtraction
print(5 * 2)            #multiplication
print(5 / 2)            #decimal division
print(5 // 2)           #floor division
print(5 ** 2)           #raised to the power
print(5 % 2)            #modulo operator

#COMPARISION OPERATORS

print(5 > 2)
print(5 < 2)
print(5 >= 2)
print(5 <= 2)
print(5 == 2)
print(5 != 2)

#LOGICAL OPERATORS

print(5 > 2 or 5 < 2)   #logical OR operator
print(5 > 2 and 5 < 2)  #logical AND operator
print(not 2 > 3)        #logical NOT operator
